package day9;

public class Account {
	
	private int accno;
	private String name;
	private double amt;
	
	
	public int getAccno() {
		return accno;   
	}
	public void setAccno(int accno){
		this.accno= accno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getAmt() {
		return amt;
	}
	public void setAmt(double amt) {
		this.amt = amt;
	}
	
}
